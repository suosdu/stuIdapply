package com.stu_id_apply.util;

public class ImportInfo {
	private String xh;
	private String qsz;
	private String zdz;
	/**
	 * @return the xh
	 */
	public String getXh() {
		return xh;
	}

	/**
	 * @param xh the xh to set
	 */
	public void setXh(String xh) {
		this.xh = xh;
	}

	public String getQsz() {
		return qsz;
	}

	public void setQsz(String qsz) {
		this.qsz = qsz;
	}

	public String getZdz() {
		return zdz;
	}

	public void setZdz(String zdz) {
		this.zdz = zdz;
	}
}
